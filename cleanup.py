import os
import shutil
import logging

# Configure logging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

def organize_project():
    """Organize project files into logical directories."""
    
    logger.info("ğŸ§¹ ORGANIZING TAP PROJECT FILES")
    logger.info("=" * 50)
    
    # Create organization directories
    directories = {
        'outputs': 'Generated outputs and visualizations',
        'backup': 'Backup and legacy files',
        'examples': 'Example and demo files'
    }
    
    for dir_name, description in directories.items():
        if not os.path.exists(dir_name):
            os.makedirs(dir_name)
            logger.info(f"âœ… Created directory: {dir_name}/ - {description}")
    
    # File organization mapping
    file_moves = {
        # Generated outputs
        'classical_designs.json': 'outputs/',
        'classical_designs.svg': 'outputs/',
        'training_progress.png': 'outputs/',
        'quantum_demo.json': 'outputs/',
        'quantum_demo.svg': 'outputs/',
        'quantum_bezier_curves.svg': 'outputs/',
        'hybrid_quantum_classical_curves.svg': 'outputs/',
        'synthetic_train.npy': 'outputs/',
        
        # Backup files
        'main_backup.py': 'backup/',
        'main_redirect.py': 'backup/',
        'analyze_data.py': 'backup/',
        
        # Examples and demos
        'demo_quantum_gan.py': 'examples/',
        'quick_demo.py': 'examples/',
        'test_bezier_gan.py': 'examples/',
    }
    
    # Move files
    for file_name, target_dir in file_moves.items():
        if os.path.exists(file_name):
            try:
                target_path = os.path.join(target_dir, file_name)
                if os.path.exists(target_path):
                    logger.info(f"âš ï¸  Skipping {file_name} - already exists in {target_dir}")
                else:
                    shutil.move(file_name, target_path)
                    logger.info(f"ğŸ“ Moved {file_name} -> {target_dir}")
            except Exception as e:
                logger.error(f"âŒ Error moving {file_name}: {e}")
    
    # Clean up cache
    cache_dirs = ['__pycache__', 'src/__pycache__']
    for cache_dir in cache_dirs:
        if os.path.exists(cache_dir):
            try:
                shutil.rmtree(cache_dir)
                logger.info(f"ğŸ—‘ï¸  Cleaned cache: {cache_dir}")
            except Exception as e:
                logger.error(f"âŒ Error cleaning {cache_dir}: {e}")
    
    logger.info("\nğŸ‰ PROJECT ORGANIZATION COMPLETED!")
    logger.info("=" * 50)
    logger.info("Project structure:")
    logger.info("  ğŸ“ outputs/ - Generated files and visualizations")
    logger.info("  ğŸ“ backup/ - Legacy and backup files")
    logger.info("  ğŸ“ examples/ - Demo and example scripts")
    logger.info("  ğŸ“ src/ - Core source code modules")
    logger.info("  ğŸ“„ main.py - Primary entry point")
    logger.info("  ğŸ“„ classical_gan.py - Professional GAN implementation")

